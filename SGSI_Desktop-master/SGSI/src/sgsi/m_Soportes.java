    public JRadioButton[] AgregarPreguntas(JRadioButton... arrayOpciones) {
        JRadioButton[] preguntas = new JRadioButton[arrayOpciones.length];
        for (int i = 0; i < arrayOpciones.length; i++) {
            preguntas[i] = arrayOpciones[i];
        }
        return preguntas;
    }

